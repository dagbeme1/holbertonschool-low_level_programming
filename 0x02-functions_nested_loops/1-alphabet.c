#include <stdlib.h>
#include <time.h>
#include "main.h"

/**
 * print_alphabet - check the code
 *
 * print_alphabet - print lowercase alphabet
 * Return: Always 0.
 */
void print_alphabet(void)
{

	char bet = 'a';
	/* for loop */
	for (bet = 'a'; bet <= 'z'; bet++)
	{
	_putchar(bet);
	}
	_putchar('\n');

}
